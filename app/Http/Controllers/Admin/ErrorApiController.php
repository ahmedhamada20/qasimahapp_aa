    {

        $this->validate($request, [
            'notes_ar' => 'required',
            'notes_en' => 'required',
        ]);

       ErrorApi::updateOrCreate(
            ['id' => $request->id], 
            [
                'version' => $request->version,
                'error_version' => $request->error_version,
            ]
        );


        toast('تمت العملية بنجاح', 'success', 'left');
        return redirect()->route('errorApi.index');
    }


